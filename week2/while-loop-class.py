# Python While Loops
# 🔍 What is a While Loop?
# It repeats code as long as a condition is true.


# 📌 Note: Be careful with while loops — they can become infinite loops.

# 💼 Real-world use:

# Retry until user gives correct password

# Loading bars or progress checks

count = 1

while count <= 10:
  print("Count: ", count)
  count += 1



