# Python If...Else
# 🔍 What is it?
# if...else is how Python makes decisions.

# Just like how you think:
# "If it's raining, take an umbrella. Else, enjoy the sun!"


# 💼 Real-world Use:
# Used in:

# Login checks: If password is correct → login, else → error

# E-commerce: If cart is empty → show message, else → show checkout


age = 17
if age >= 18:
  print("You can go abroad for study!")
else:
  print("You are not allowed to go abroad for study")